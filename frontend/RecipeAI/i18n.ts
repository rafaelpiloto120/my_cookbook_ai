async function getInitialLanguage(): Promise<string> {
  // 1) If user already chose a language, honor it
  try {
    const storedLang = await AsyncStorage.getItem("userLanguage");
    if (storedLang) return storedLang;
  } catch (e) {
    console.warn("Failed to load language from storage:", e);
  }

  // 2) Try SDK 50+ API first
  try {
    const locales = (Localization as any).getLocales?.();
    const tag: string | undefined = Array.isArray(locales) && locales.length > 0
      ? (locales[0].languageTag || locales[0].localeIdentifier || locales[0].languageCode)
      : undefined;
    const raw = (tag || (Localization as any).locale || "en").toLowerCase();
    if (raw.startsWith("pt-br")) return "pt-BR";
    if (raw.startsWith("pt")) return "pt";
    if (raw.startsWith("es")) return "es";
    if (raw.startsWith("fr")) return "fr";
    if (raw.startsWith("de")) return "de";
    return "en";
  } catch {
    // 3) Fallback to legacy `Localization.locale`
    let deviceLang = ((Localization as any).locale || "en").toLowerCase();
    if (deviceLang.startsWith("pt-br")) return "pt-BR";
    if (deviceLang.startsWith("pt")) return "pt";
    if (deviceLang.startsWith("es")) return "es";
    if (deviceLang.startsWith("fr")) return "fr";
    if (deviceLang.startsWith("de")) return "de";
    return "en";
  }
}


// Initialize i18n directly with the correct initial language
(async () => {
  const initialLang = await getInitialLanguage();
  i18n.use(initReactI18next).init({
    compatibilityJSON: "v3",
    resources,
    lng: initialLang,
    fallbackLng: "en",
    supportedLngs: ["en", "es", "pt", "pt-BR", "fr", "de"],
    nonExplicitSupportedLngs: true,
    cleanCode: true,
    lowerCaseLng: true,
    returnNull: false,
    interpolation: { escapeValue: false },
  });
})();

